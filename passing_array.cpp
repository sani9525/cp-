#include <bits/stdc++.h>
using namespace std;
void printArray(int input[], int n){
    for(int i=0; i<n; i++){
        cout<<input[i]<<" ";
    }
    cout<<endl;
}
int main (){
    int n;
    cin>>n;
    int input[n];
    
    for(int i=0; i<n; i++){
        cin>>n;
    }
    printArray(input,n);

}